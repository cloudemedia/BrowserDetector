    /**
     * @var \BrowserDetector\Loader\ExtendedLoaderInterface
     */
    private $loader;

    /**
     * @param \BrowserDetector\Loader\ExtendedLoaderInterface $loader
     */
    public function __construct(ExtendedLoaderInterface $loader)
    {
        $this->loader = $loader;
    }

    /**
     * detects the device name from the given user agent
     *
     * @param string           $useragent
     * @param \Stringy\Stringy $s
     *
     * @return array
     */
    public function detect(string $useragent, Stringy $s): array
    {
        return $this->detectInArray($this->devices, $useragent, $s);
    }

    /**
     * @param array            $devices
     * @param string           $useragent
     * @param \Stringy\Stringy $s
     *
     * @return array
     */
    private function detectInArray(array $devices, string $useragent, Stringy $s): array
    {
        $matches = [];

        if (preg_match('/((gt|sam|sc|sch|sec|sgh|shv|shw|sm|sph|continuum|ek|yp)\-[0-9a-z]+)/i', $useragent, $matches)) {
            $key = 'samsung ' . mb_strtolower($matches[1]);

            if ($this->loader->has($key)) {
                return $this->loader->load($key, $useragent);
            }
        }

        foreach ($devices as $search => $key) {
            if (!$s->contains($search, false)) {
                continue;
            }

            if (is_array($key)) {
                return $this->detectInArray($key, $useragent, $s);
            }

            return $this->loader->load($key, $useragent);
        }

        return $this->loader->load('general samsung device', $useragent);
    }
